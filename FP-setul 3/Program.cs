                if (g[p] == c[id]) { p++;id++; }
                if (p == M) { nr++;
                    p = lps[p - 1];
                }
                else if (id < N && g[p] != c[id])
                {
                    if (p != 0) p = lps[p - 1];
                    else id++;
                }
            }
