        private static void quicks(int[] v,int lo, int hi)
        { Random rnd = new Random();
            if (lo < hi) {
                int index;
                index = rnd.Next(lo, hi + 1);
                (v[lo], v[index]) = (v[index], v[lo]);
                int k = lo;
                for(int i = lo + 1; i <= hi; i++) if (v[i] < v[lo])
                    {
                        k++;
                        (v[k], v[i]) = (v[i], v[k]);
                    }
                (v[lo], v[k]) = (v[k], v[lo]);
                quicks(v, lo, k - 1);
                quicks(v, k + 1, hi);
                    }
        }
